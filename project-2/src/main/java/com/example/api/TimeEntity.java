package com.example.api;

import java.util.Date;

public class TimeEntity {
	private Date date;
	
	public TimeEntity() {
		super();
	}

	public TimeEntity(Date date) {
		super();
		this.date = date;
	}

	public Date getDate() {
		return date;
	}

	public void setDate(Date date) {
		this.date = date;
	}
	
	
}
